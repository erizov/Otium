from typing import NotRequired, TypedDict


class Osobnjak(TypedDict):
    """Описание городского особняка."""

    name: str
    address: str
    style: str
    highlights: list[str]
    history: str
    significance: str
    facts: list[str]
    images: list[str]
    lat: float
    lon: float
    name_en: NotRequired[str]
    history_en: NotRequired[str]
    significance_en: NotRequired[str]


IMAGES_SUBFOLDER = "spb_osobnjaki"


def _img(rel_path: str) -> str:
    return "images/{}/{}".format(IMAGES_SUBFOLDER, rel_path)


def _o(
    name: str,
    address: str,
    style: str,
    highlights: list[str],
    history: str,
    significance: str,
    facts: list[str],
    imgs: list[str],
    lat: float,
    lon: float,
    *,
    name_en: str | None = None,
    history_en: str | None = None,
    significance_en: str | None = None,
) -> Osobnjak:
    row: Osobnjak = {
        "name": name,
        "address": address,
        "style": style,
        "highlights": highlights,
        "history": history,
        "significance": significance,
        "facts": facts,
        "images": [_img(i) for i in imgs],
        "lat": lat,
        "lon": lon,
    }
    if name_en:
        row["name_en"] = name_en
    if history_en:
        row["history_en"] = history_en
    if significance_en:
        row["significance_en"] = significance_en
    return row


