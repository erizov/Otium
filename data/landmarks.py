"""Данные об iconic landmarks Москвы для путеводителя."""

from typing import TypedDict


class Landmark(TypedDict):
    """Описание iconic landmark."""

    name: str
    address: str
    style: str
    highlights: list[str]
    history: str
    significance: str
    facts: list[str]
    images: list[str]
    lat: float
    lon: float


IMAGES_SUBFOLDER = "moscow_landmarks"


def _img(rel_path: str) -> str:
    """Путь к изображению: output/images/moscow_landmarks/<rel_path>."""
    return "images/{}/{}".format(IMAGES_SUBFOLDER, rel_path)


def _l(name: str, address: str, style: str, highlights: list[str],
       history: str, significance: str, facts: list[str], imgs: list[str],
       lat: float, lon: float,
       aliases: list[str] | None = None,
       name_en: str | None = None) -> Landmark:
    """Сокращение для записи landmark."""
    d: Landmark = {
        "name": name,
        "address": address,
        "style": style,
        "highlights": highlights,
        "history": history,
        "significance": significance,
        "facts": facts,
        "images": [_img(i) for i in imgs],
        "lat": lat,
        "lon": lon,
    }
    if aliases:
        d["aliases"] = aliases  # type: ignore[typeddict-unknown-key]
    if name_en:
        d["name_en"] = name_en  # type: ignore[typeddict-unknown-key]
    return d


