from typing import TypedDict


class Cafe(TypedDict):
    """Описание кафе."""

    name: str
    address: str
    style: str
    highlights: list[str]
    history: str
    significance: str
    facts: list[str]
    images: list[str]
    lat: float
    lon: float


IMAGES_SUBFOLDER = "moscow_cafes"


def _img(rel_path: str) -> str:
    """Путь к изображению: output/images/moscow_cafes/<rel_path>."""
    return "images/{}/{}".format(IMAGES_SUBFOLDER, rel_path)


def _c(name: str, address: str, style: str, highlights: list[str],
       history: str, significance: str, facts: list[str], imgs: list[str],
       lat: float, lon: float) -> Cafe:
    """Сокращение для записи кафе."""
    return {
        "name": name,
        "address": address,
        "style": style,
        "highlights": highlights,
        "history": history,
        "significance": significance,
        "facts": facts,
        "images": [_img(i) for i in imgs],
        "lat": lat,
        "lon": lon,
    }


