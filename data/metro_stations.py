IMAGES_SUBFOLDER = "moscow_metro"


def _img(rel_path: str) -> str:
    return "images/{}/{}".format(IMAGES_SUBFOLDER, rel_path)


def _ms(name: str, line: str, style: str, highlights: list[str],
        history: str, significance: str, facts: list[str], imgs: list[str],
        lat: float, lon: float) -> MetroStation:
    return {
        "name": name,
        "address": line,
        "style": style,
        "highlights": highlights,
        "history": history,
        "significance": significance,
        "facts": facts,
        "images": [_img(i) for i in imgs],
        "lat": lat,
        "lon": lon,
    }


