# -*- coding: utf-8 -*-
"""Данные о вокзалах Москвы для путеводителя."""

from typing import TypedDict


class RailwayStation(TypedDict):
    """Описание вокзала."""

    name: str
    address: str
    style: str
    highlights: list[str]
    history: str
    significance: str
    facts: list[str]
    images: list[str]
    lat: float
    lon: float


IMAGES_SUBFOLDER = "moscow_railway_stations"


def _img(rel_path: str) -> str:
    """Путь к изображению: output/images/moscow_railway_stations/<rel_path>."""
    return "images/{}/{}".format(IMAGES_SUBFOLDER, rel_path)


def _rs(name: str, address: str, style: str, highlights: list[str],
        history: str, significance: str, facts: list[str], imgs: list[str],
        lat: float, lon: float) -> RailwayStation:
    """Сокращение для записи вокзала."""
    return {
        "name": name,
        "address": address,
        "style": style,
        "highlights": highlights,
        "history": history,
        "significance": significance,
        "facts": facts,
        "images": [_img(i) for i in imgs],
        "lat": lat,
        "lon": lon,
    }


