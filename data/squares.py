"""Данные о площадях Москвы для путеводителя."""

from typing import TypedDict


class Square(TypedDict):
    """Описание площади."""

    name: str
    address: str
    style: str
    highlights: list[str]
    history: str
    significance: str
    facts: list[str]
    images: list[str]
    lat: float
    lon: float


IMAGES_SUBFOLDER = "moscow_squares"


def _img(rel_path: str) -> str:
    """Путь к изображению: output/images/moscow_squares/<rel_path>."""
    return "images/{}/{}".format(IMAGES_SUBFOLDER, rel_path)


def _sq(name: str, address: str, style: str, highlights: list[str],
        history: str, significance: str, facts: list[str], imgs: list[str],
        lat: float, lon: float) -> Square:
    """Сокращение для записи площади."""
    return {
        "name": name,
        "address": address,
        "style": style,
        "highlights": highlights,
        "history": history,
        "significance": significance,
        "facts": facts,
        "images": [_img(i) for i in imgs],
        "lat": lat,
        "lon": lon,
    }


