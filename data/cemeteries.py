# -*- coding: utf-8 -*-
"""Данные о некрополях и кладбищах Москвы для путеводителя."""

from typing import TypedDict


class Cemetery(TypedDict):
    """Описание некрополя или кладбища."""

    name: str
    address: str
    style: str
    highlights: list[str]
    history: str
    significance: str
    facts: list[str]
    images: list[str]
    lat: float
    lon: float


IMAGES_SUBFOLDER = "moscow_cemeteries"


def _img(rel_path: str) -> str:
    """Путь к изображению: output/images/moscow_cemeteries/<rel_path>."""
    return "images/{}/{}".format(IMAGES_SUBFOLDER, rel_path)


def _c(name: str, address: str, style: str, highlights: list[str],
       history: str, significance: str, facts: list[str], imgs: list[str],
       lat: float, lon: float,
       aliases: list[str] | None = None,
       name_en: str | None = None) -> Cemetery:
    """Сокращение для записи некрополя."""
    d: Cemetery = {
        "name": name,
        "address": address,
        "style": style,
        "highlights": highlights,
        "history": history,
        "significance": significance,
        "facts": facts,
        "images": [_img(i) for i in imgs],
        "lat": lat,
        "lon": lon,
    }
    if aliases:
        d["aliases"] = aliases  # type: ignore[typeddict-unknown-key]
    if name_en:
        d["name_en"] = name_en  # type: ignore[typeddict-unknown-key]
    return d


