from typing import TypedDict


class Sculpture(TypedDict):
    name: str
    address: str
    style: str
    highlights: list[str]
    history: str
    significance: str
    facts: list[str]
    images: list[str]
    lat: float
    lon: float


IMAGES_SUBFOLDER = "moscow_sculptures"


def _img(rel_path: str) -> str:
    return "images/{}/{}".format(IMAGES_SUBFOLDER, rel_path)


def _s(n: str, a: str, s: str, h: list[str], hist: str, sig: str, f: list[str],
       imgs: list[str], lat: float, lon: float) -> Sculpture:
    return {"name": n, "address": a, "style": s, "highlights": h,
            "history": hist, "significance": sig, "facts": f,
            "images": [_img(i) for i in imgs], "lat": lat, "lon": lon}


