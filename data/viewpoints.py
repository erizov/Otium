"""Данные о смотровых площадках Москвы для путеводителя."""

from typing import TypedDict


class Viewpoint(TypedDict):
    """Описание смотровой площадки."""

    name: str
    address: str
    style: str
    highlights: list[str]
    history: str
    significance: str
    facts: list[str]
    images: list[str]
    lat: float
    lon: float


IMAGES_SUBFOLDER = "moscow_viewpoints"


def _img(rel_path: str) -> str:
    """Путь к изображению: output/images/moscow_viewpoints/<rel_path>."""
    return "images/{}/{}".format(IMAGES_SUBFOLDER, rel_path)


def _v(name: str, address: str, style: str, highlights: list[str],
       history: str, significance: str, facts: list[str], imgs: list[str],
       lat: float, lon: float) -> Viewpoint:
    """Сокращение для записи смотровой площадки."""
    return {
        "name": name,
        "address": address,
        "style": style,
        "highlights": highlights,
        "history": history,
        "significance": significance,
        "facts": facts,
        "images": [_img(i) for i in imgs],
        "lat": lat,
        "lon": lon,
    }


