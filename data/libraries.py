"""Данные о библиотеках Москвы для путеводителя."""

from typing import TypedDict


class Library(TypedDict):
    """Описание библиотеки."""

    name: str
    address: str
    style: str
    highlights: list[str]
    history: str
    significance: str
    facts: list[str]
    images: list[str]
    lat: float
    lon: float


IMAGES_SUBFOLDER = "moscow_libraries"


def _img(rel_path: str) -> str:
    """Путь к изображению: output/images/moscow_libraries/<rel_path>."""
    return "images/{}/{}".format(IMAGES_SUBFOLDER, rel_path)


def _lib(name: str, address: str, style: str, highlights: list[str],
         history: str, significance: str, facts: list[str], imgs: list[str],
         lat: float, lon: float) -> Library:
    """Сокращение для записи библиотеки."""
    return {
        "name": name,
        "address": address,
        "style": style,
        "highlights": highlights,
        "history": history,
        "significance": significance,
        "facts": facts,
        "images": [_img(i) for i in imgs],
        "lat": lat,
        "lon": lon,
    }


